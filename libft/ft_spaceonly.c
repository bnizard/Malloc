#include "libft.h"

void	ft_spaceonly(char **line)
{
	int		i;

	i = 0;
	while ((*line)[i])
	{
		if ((*line)[i] == '\t' || (*line)[i] == '\n')
			(*line)[i] = ' ';
		i++;
	}
}
